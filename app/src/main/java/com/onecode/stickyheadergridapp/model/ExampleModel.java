package com.onecode.stickyheadergridapp.model;

public class ExampleModel {

    private String title;
    private int number;

    public ExampleModel(String title, int number) {
        this.title = title;
        this.number = number;
    }

    public String getTitle() {
        return title;
    }

    public int getNumber() {
        return number;
    }
}
